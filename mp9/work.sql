CREATE TABLE flights (
    RID INT UNSIGNED NOT NULL,
    THE_YEAR YEAR NOT NULL,
    THE_MONTH TINYINT UNSIGNED NOT NULL,
    THE_DAY TINYINT UNSIGNED NOT NULL,
    DAY_OF_WEEK TINYINT UNSIGNED NOT NULL,
    AIRLINE VARCHAR(10) NOT NULL,
    FLIGHT_NUMBER MEDIUMINT UNSIGNED NOT NULL,
    TAIL_NUMBER VARCHAR(10) NOT NULL,
    ORIGIN_AIRPORT VARCHAR(5) NOT NULL,
    DESTINATION_AIRPORT VARCHAR(5) NOT NULL,
    SCHEDULED_DEPARTURE	INT NOT NULL,
    DEPARTURE_TIME INT NOT NULL,
    DEPARTURE_DELAY INT NOT NULL,
    TAXI_OUT INT NOT NULL,
    WHEELS_OFF INT NOT NULL,
    SCHEDULED_TIME INT NOT NULL,
    ELAPSED_TIME INT NOT NULL,
    AIR_TIME INT NOT NULL,
    DISTANCE INT NOT NULL,
    WHEELS_ON INT NOT NULL,
    TAXI_IN INT NOT NULL,
    SCHEDULED_ARRIVAL INT NOT NULL,
    ARRIVAL_TIME INT NOT NULL,
    ARRIVAL_DELAY INT NOT NULL,
    DIVERTED INT NOT NULL,
    CANCELLED INT NOT NULL,
    CANCELLATION_REASON VARCHAR(5),
    AIR_SYSTEM_DELAY INT,
    SECURITY_DELAY INT,
    AIRLINE_DELAY INT,
    LATE_AIRCRAFT_DELAY INT,
    WEATHER_DELAY INT,
    CONSTRAINT rid_pk PRIMARY KEY (rid)
);